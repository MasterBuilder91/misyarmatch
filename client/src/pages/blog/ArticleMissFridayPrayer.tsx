      <p>Allah says: <em>"O you who have believed, when the call to prayer is made on the day of Jumu'ah, then proceed to the remembrance of Allah and leave trade."</em> (62:9) The command is direct: when the adhan is called on Friday, leaving business and going to the prayer is obligatory for those it applies to.</p>

